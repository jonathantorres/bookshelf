#include <stdio.h>

#define N 5

int sumarray3d(int a[N][N][N])
{
    int i, j, k, sum = 0;

    for (k = 0; k < N; k++) {
        for (i = 0; i < N; i++) {
            for (j = 0; j < N; j++) {
                sum += a[k][i][j];
            }
        }
    }

    return sum;
}

int main(void)
{
    int a[N][N][N] = {
        {
            {1, 5, 1, 8, 8},
            {2, 5, 7, 2, 8},
            {9, 2, 1, 9, 8},
            {9, 5, 8, 9, 2},
            {2, 7, 1, 0, 8},
        },
        {
            {8, 5, 1, 2, 8},
            {1, 8, 7, 6, 8},
            {1, 5, 1, 6, 8},
            {8, 8, 6, 9, 8},
            {1, 5, 1, 7, 8},
        },
        {
            {2, 5, 4, 4, 8},
            {4, 2, 1, 8, 4},
            {1, 5, 2, 4, 8},
            {8, 4, 4, 9, 4},
            {1, 5, 6, 8, 8},
        },
        {
            {1, 5, 6, 9, 9},
            {9, 9, 1, 9, 9},
            {2, 2, 1, 9, 8},
            {1, 5, 6, 9, 9},
            {9, 2, 2, 9, 8},
        },
        {
            {3, 7, 6, 9, 8},
            {1, 5, 1, 3, 7},
            {7, 9, 3, 6, 9},
            {3, 3, 6, 9, 9},
            {1, 5, 6, 3, 3},
        },
    };

    printf("%d\n", sumarray3d(a));

    return 0;
}
